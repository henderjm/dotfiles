return {
    'goolord/alpha-nvim',
    dependencies = { 'nvim-tree/nvim-web-devicons' },
    config = function ()
        local alpha = require('alpha')
        local dashboard = require('alpha.themes.dashboard')
        math.randomseed(os.time())
        local logo = logos[math.random(#logos)]
        dashboard.section.header.val = logo

        require('alpha').setup({
            event = "VimEnter",
            layout = {
                { type = "padding", val = 2 },
                buttons = {
                    type = "group",
                    val = {
                        {
                            type = "text",
                            val = dashboard.section.header.val,
                            opts = {
                                position = "center",
                                hl = "AlphaHeader",
                            },
                        },
                        { type = "padding", val = 5 },
                        { type = "text", val = "Menu", opts = { hl = "SpecialComment", position = "center" } },
                        { type = "padding", val = 1 },
                        dashboard.button("p", "  Search projects", ":Telescope project<CR>"),
                        dashboard.button("f", "  Find file", ":Telescope find_files <CR>"),
                        dashboard.button("b", "  File Browser", ":Telescope file_browser <CR>"),
                        dashboard.button("t", "  Find text", ":Telescope live_grep <CR>"),
                        dashboard.button("r", "  Recent files", ":Telescope oldfiles <CR>"),
                        dashboard.button("c", "  Configuration", ":lua require('telescope.builtin').find_files({cwd = '~/.config/nvim/' })<CR>"),
                        dashboard.button("q", "  Quit", ":qa<CR>"),
                        { type = "padding", val = 1 },
                    },
                },
            },
            opts = {
                margin = 5,
            }
        })
    end
}
